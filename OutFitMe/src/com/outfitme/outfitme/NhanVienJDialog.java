    NhanVienDAO dao = new NhanVienDAO();
    int row = -1;

    private void init() {
        setLocationRelativeTo(null);
        this.fillTable();
    }

    private void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);
        try {
            List<NhanVien> list = dao.selectAll();
            for (NhanVien nv : list) {
                Object[] row = {
                    nv.getMaNV(),
                    nv.getTenNV(),
                    nv.getNgaySinh(),
                    nv.isGioiTinh() ? "Nam" : "Nữ",
                    nv.getSoDienThoai(),
                    nv.getDiaChi(),
                    nv.getTenDangNhap(),
                    nv.getMatKhau(),
                    nv.isChucVu() ? "Chủ Shop" : "Nhân viên"
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }

    private void setForm(NhanVien nv) {
        txtMaNV.setText(nv.getMaNV());
        txtTenNV.setText(nv.getTenNV());
        txtNgaySinh.setText(XDate.toString(nv.getNgaySinh(), "yyyy-MM-dd"));
        if (nv.isGioiTinh()) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
        txtSDT.setText(nv.getSoDienThoai());
        txtDiaChi.setText(nv.getDiaChi());
        txtTaiKhoan.setText(nv.getTenDangNhap());
        txtMatKhau.setText(nv.getMatKhau());
        if (nv.isChucVu()) {
            rdoChuSop.setSelected(true);
        } else {
            rdoNhanVien.setSelected(true);
        }
    }

    NhanVien getForm() {
        NhanVien nv = new NhanVien();
        nv.setMaNV(txtMaNV.getText());
        nv.setTenNV(txtTenNV.getText());
        nv.setNgaySinh(XDate.toDate(txtNgaySinh.getText(), "yyyy-MM-dd"));
        nv.setGioiTinh(rdoNam.isSelected());
        nv.setSoDienThoai(txtSDT.getText());
        nv.setDiaChi(txtDiaChi.getText());
        nv.setTenDangNhap(txtTaiKhoan.getText());
        nv.setMatKhau(txtMatKhau.getText());
        nv.setChucVu(rdoChuSop.isSelected());
        return nv;
    }

    boolean isValidate() {
        if (txtMaNV.getText().trim().isEmpty()
                || txtTenNV.getText().trim().isEmpty()
                || txtNgaySinh.getText().trim().isEmpty()
                || txtSDT.getText().trim().isEmpty()
                || txtDiaChi.getText().trim().isEmpty()
                || txtTaiKhoan.getText().trim().isEmpty()
                || txtMatKhau.getText().trim().isEmpty()
                || (!rdoNam.isSelected() && !rdoNu.isSelected())
                || (!rdoChuSop.isSelected() && !rdoNhanVien.isSelected())) {

            MsgBox.alert(this, "Vui lòng nhập đầy đủ thông tin!");
            return false;
        }
        return true;
    }

    private void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblNhanVien.getRowCount() - 1);
        // Trạng thái form
        txtMaNV.setEditable(!edit);
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);

        // Trạng thái điều hướng
        btnFirst.setEnabled(edit && !first);
        btnPreV.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }

    private void insert() {
        NhanVien nv = this.getForm();
        if (!isValidate()) {
            return;
        }
        try {
            dao.insert(nv);
            this.fillTable();
            this.clearForm();
            MsgBox.alert(this, "Thêm mới thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Thêm mới thất bại!");
        }
    }

    private void clearForm() {
        txtMaNV.setText("");
        txtTenNV.setText("");
        txtNgaySinh.setText("");
        txtSDT.setText("");
        txtDiaChi.setText("");
        txtTaiKhoan.setText("");
        txtMatKhau.setText("");

        // Bo chon tat ca nut rdo
        rdoNam.setSelected(false);
        rdoNu.setSelected(false);
        rdoChuSop.setSelected(false);
        rdoNhanVien.setSelected(false);

        this.row = -1;
        updateStatus();
    }

    private void update() {
        NhanVien nv = this.getForm();
        if (!isValidate()) {
            return;
        }
        try {
            dao.update(nv);
            this.fillTable();
            this.clearForm();
            MsgBox.alert(this, "Sửa thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Sửa thất bại!");
        }
    }

    private void delete() {
        if (!Auth.isManager()) {
            MsgBox.alert(this, "Bạn không có quyền xóa nhân viên!");
        } else {
            String manv = txtMaNV.getText();
            if (manv.equals(Auth.user.getMaNV())) {
                MsgBox.alert(this, "Bạn không được xóa chính bạn!");
            } else if (MsgBox.confirm(this, "Bạn thực sự muốn xóa nhân viên này?")) {
                try {
                    dao.delete(manv);
                    this.fillTable();
                    this.clearForm();
                    MsgBox.alert(this, "Xóa thành công!");
                } catch (Exception e) {
                    MsgBox.alert(this, "Xóa thất bại!");
                }
            }
        }
    }

    void edit() {
        String manv = (String) tblNhanVien.getValueAt(this.row, 0);
        NhanVien nv = dao.selectByIdMaNV(manv);
        this.setForm(nv);
        tabs.setSelectedIndex(0);
        this.updateStatus();
    }

    void first() {
        this.row = 0;
        this.edit();
    }

    void prev() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }
    }

    void next() {
        if (this.row < tblNhanVien.getRowCount() - 1) {
            this.row++;
            this.edit();
        }
    }

    void last() {
        this.row = tblNhanVien.getRowCount() - 1;
        this.edit();
    }
