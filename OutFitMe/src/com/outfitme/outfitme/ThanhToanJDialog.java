    private void ThanhToan(){
         if (!jRadioButton1.isSelected() && !jRadioButton2.isSelected()) {
            JOptionPane.showMessageDialog(this, "Vui l√≤ng ch·ªçn h√¨nh th·ª©c thanh to√°n!");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this,
                "B·∫°n c√≥ ch·∫Øc ch·∫Øn mu·ªën thanh to√°n h√≥a ƒë∆°n n√†y?",
                "X√°c nh·∫≠n thanh to√°n",
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            int selectedIndex = cboDSKhachHang.getSelectedIndex();
            String soHD = txtSoHD.getText().trim();

            if (selectedIndex >= 0 && !soHD.isEmpty()) {
                String selectedCustomer = (String) cboDSKhachHang.getSelectedItem();
                String maKH = selectedCustomer.split(" - ")[0];
                KhachHang kh = khDao.selectById(maKH);

                if (kh != null) {
                    // T√≠nh ƒëi·ªÉm t√≠ch l≈©y
                    int newPoints = calculatePoints(totalPrice);
                    int currentPoints = kh.getDiem();

                    int pointsToDeduct = 0;
                    if (discountPercentage == 5.0) {
                        pointsToDeduct = 50;
                    } else if (discountPercentage == 10.0) {
                        pointsToDeduct = 100;
                    } else if (discountPercentage == 15.0) {
                        pointsToDeduct = 200;
                    }

                    int updatedPoints = currentPoints + newPoints - pointsToDeduct;
                    if (updatedPoints < 0) {
                        updatedPoints = 0;
                    }
                    kh.setDiem(updatedPoints);
                    khDao.update(kh);

                    // L∆∞u l·ªãch s·ª≠
                    savePurchaseHistory(maKH);

                    // üî• X√≥a duy nh·∫•t h√≥a ƒë∆°n ƒë√£ nh·∫≠p
                    cthdDao.deleteBySoHD(soHD); // ‚Üê b·∫°n c·∫ßn c√≥ h√†m delete theo S·ªë Hƒê

                    // Load l·∫°i b·∫£ng
                    filterTableByMaKH();

                    // Reset c√°c gi√° tr·ªã
                    totalPrice = 0.0;
                    discountPercentage = 0.0;
                    rdo5.setSelected(false);
                    rdo10.setSelected(false);
                    rdo15.setSelected(false);
                    updateTotalPriceWithDiscount();
                    jRadioButton1.setSelected(false);
                    jRadioButton2.setSelected(false);
                    txtSoHD.setText("");

                    JOptionPane.showMessageDialog(this, "Thanh to√°n th√†nh c√¥ng!", "Th√¥ng b√°o", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Vui l√≤ng ch·ªçn kh√°ch h√†ng v√† nh·∫≠p s·ªë h√≥a ƒë∆°n!");
            }
        }


    }
