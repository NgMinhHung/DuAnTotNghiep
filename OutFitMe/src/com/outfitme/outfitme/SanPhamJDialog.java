    private void loadTable() {
        if (cboLoai.getSelectedItem() == null) {
            System.out.println("‚ö† L·ªói: JComboBox ch∆∞a c√≥ gi√° tr·ªã!");
            return;
        }
        String loaiSP = cboLoai.getSelectedItem().toString().trim();
        System.out.println("üìå Lo·∫°i s·∫£n ph·∫©m ƒë√£ ch·ªçn: " + loaiSP);

        DefaultTableModel model = (DefaultTableModel) tblSP.getModel();
        model.setRowCount(0);  // X√≥a d·ªØ li·ªáu c≈© tr∆∞·ªõc khi th√™m m·ªõi

        try {
            List<SanPham> list = dao.selectByLoaiSP(loaiSP);
            for (SanPham sp : list) {
                model.addRow(new Object[]{
                    sp.getMaSP(), sp.getTenSP(), sp.getLoaiSP(),
                    sp.getMoTa(), sp.getGiaNhap(), sp.getGiaBan(),
                    sp.getSize(), sp.getSoLuongTonKho(), sp.getPhanLoai()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "‚ö† L·ªói khi t·∫£i d·ªØ li·ªáu s·∫£n ph·∫©m!\n" + e.getMessage());
            e.printStackTrace();
        }
    }

    private void loadLoaiSanPham() {
        cboLoai.removeAllItems(); // X√≥a d·ªØ li·ªáu c≈© tr∆∞·ªõc khi load m·ªõi
        try {
            List<String> listLoaiSP = dao.getAllLoaiSanPham();
            for (String loai : listLoaiSP) {
                cboLoai.addItem(loai);
            }
            if (cboLoai.getItemCount() > 0) {
                cboLoai.setSelectedIndex(0); // Ch·ªçn gi√° tr·ªã ƒë·∫ßu ti√™n m·∫∑c ƒë·ªãnh
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "‚ö† L·ªói khi t·∫£i lo·∫°i s·∫£n ph·∫©m: " + e.getMessage());
        }
    }

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {
        this.row = tblSP.getSelectedRow();
        if (this.row >= 0) {
            fillFormFromTable();
        }
    }

    private void fillFormFromTable() {
        if (this.row < 0) {
            return;
        }
        String maSP = (String) tblSP.getValueAt(this.row, 0);
        SanPham sp = dao.selectById(maSP);
        setForm(sp);
    }

